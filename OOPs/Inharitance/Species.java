package OOPs.Inharitance;

public class Species {

    public int age;
    public boolean haveGills;
    public boolean haveLungs;

    Species(int age) {
        this.age = age;
    }

    Species(boolean haveGills, boolean haveLungs) {
        this.haveGills = haveGills;
        this.haveLungs = haveLungs;
    }

}
